package com.itschool.session4.homework.incrementanddecrementoperators;

public class ex1 {
    public static void main(String[] args) {
        for (int x = 1; x <= 10; x++) {
            System.out.print(x);
        }

        for (int x = 9; x >= 1; x--) {
            System.out.print(x);

        }
    }
}
